package encapsulation;

public class Customer
{
private	String name;
private long accno;
private	int bal;
public String getName() 
{
	return name;
}
public void setName(String name) 
{
	this.name = name;
}
public long getAccno() 
{
	return accno;
}
public void setAccno(long accno)
{
	this.accno = accno;
}
public int getBal()
{
	return bal;
}
public void setBal(int bal) 
{
	this.bal = bal;
}
	

}
